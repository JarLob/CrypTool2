              line.X1 = (x1* xSize) + paddingleft;
              line.X2 = (x2 * xSize) + paddingleft;
              line.Y1 = (y1 * ySize) ;
              line.Y2 = (y2 * ySize) ;
              //if (l.Y1 > Ordinate.Y2) l.Y1 = Ordinate.Y2;
              //if (l.Y2 > Ordinate.Y2) l.Y2 = Ordinate.Y2;

            }
          }
        }
      }

    }
