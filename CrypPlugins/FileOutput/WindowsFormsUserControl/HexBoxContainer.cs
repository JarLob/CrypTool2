    void CleanUp()
    {
      if (hexBox.ByteProvider != null)
      {
        IDisposable byteProvider = hexBox.ByteProvider as IDisposable;
        if (byteProvider != null)
          byteProvider.Dispose();
        hexBox.ByteProvider = null;
      }
      _fileName = null;
      DisplayText(null);
    }

