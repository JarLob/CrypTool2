        public void PreExecution()
        {
            EventsHelper.GuiLogMessage(OnGuiLogNotificationOccured, this, new GuiLogEventArgs("Preparing enigma for operation..", this,  NotificationLevel.Info));

            if (settings.Model != 3)
            {
                EventsHelper.GuiLogMessage(OnGuiLogNotificationOccured, this, new GuiLogEventArgs("This simulator is work in progress. As of right now only Enigma I is supported!!", this, NotificationLevel.Warning));
                return;
            }

            //configure the enigma
            configureEnimga();

            initialKey = settings.Key;
        }

        public void Execute()
        {

            if (settings.Model != 3)
            {
                EventsHelper.GuiLogMessage(OnGuiLogNotificationOccured, this, new GuiLogEventArgs("This simulator is work in progress. As of right now only Enigma I is supported!!", this, NotificationLevel.Error));
                return;
            }

            EventsHelper.GuiLogMessage(OnGuiLogNotificationOccured, this, new GuiLogEventArgs("Enigma execution started...", this, NotificationLevel.Info));

            if (inputString != null)
            {
                StringBuilder output = new StringBuilder("");
                int i = 0;
                char resultChar = '?';
                bool wasUpperCase = false;

                // re-set the key to initial value..
                settings.Key = initialKey;

                // now process each letter
                foreach (char c in inputString.ToCharArray())
                {
                    EventsHelper.ProgressChanged(OnPluginProgressChanged, this, new PluginProgressEventArgs(i++, inputString.Length));

                    wasUpperCase = char.IsUpper(c);
                    char upperC = char.ToUpper(c);

                    if (settings.Alphabet.Contains(upperC))
                    {
                        resultChar = enigmacrypt(upperC);
                            
                    }
                    else
                    {
                        switch (settings.UnknownSymbolHandling)
                        {
                            case 0: // ignore
                                resultChar = c;
                                break;
                            case 1: // remove
                                continue;
                            case 2: // replace by X
                                resultChar = enigmacrypt('X');
                                break;
                        }
                    }


                    // case handling
                    switch (settings.CaseHandling)
                    {
                        case 0: // preserve
                            if (wasUpperCase)
                                resultChar = char.ToUpper(resultChar);
                            else
                                resultChar = char.ToLower(resultChar);
                            break;
                        case 1: // upper
                            resultChar = char.ToUpper(resultChar);
                            break;
                        case 2: // lower
                            resultChar = char.ToLower(resultChar);
                            break;
                    }


                    // append the calculated value to the output
                    output.Append(resultChar);
                }

                outputString = output.ToString();
                EventsHelper.ProgressChanged(OnPluginProgressChanged, this, new PluginProgressEventArgs(100, 100));
                OnPropertyChanged("OutputString");
            }
        }

        private void configureEnimga()
        {
            // set some helper variables
            A = settings.Alphabet + settings.Alphabet + settings.Alphabet;
            aLength = settings.Alphabet.Length;

            // prepare the current string for the forward substitution. We use a the string 3-times, since this saves some alphabet wrap-arround checking
            rotor1For = rotors[settings.Model, settings.Rotor1] + rotors[settings.Model, settings.Rotor1] + rotors[settings.Model, settings.Rotor1];
            rotor2For = rotors[settings.Model, settings.Rotor2] + rotors[settings.Model, settings.Rotor2] + rotors[settings.Model, settings.Rotor2];
            rotor3For = rotors[settings.Model, settings.Rotor3] + rotors[settings.Model, settings.Rotor3] + rotors[settings.Model, settings.Rotor3];
            if (settings.Rotor4 < 8)
            {
                rotor4For = rotors[settings.Model, settings.Rotor4] + rotors[settings.Model, settings.Rotor4] + rotors[settings.Model, settings.Rotor4];
            }
            

            if (settings.Model > 0) // the Enigma A/B did not have a reflector, hence there is no reverse substitution
            {
                // prepare the current string for the reflector
                reflector = reflectors[settings.Model, settings.Reflector] + reflectors[settings.Model, settings.Reflector] + reflectors[settings.Model, settings.Reflector];

                // prepare the current string for the reverse substitution.
                rotor1Rev = generateReverseSubst(rotors[settings.Model, settings.Rotor1]);
                rotor1Rev += rotor1Rev + rotor1Rev;
                rotor2Rev = generateReverseSubst(rotors[settings.Model, settings.Rotor2]);
                rotor2Rev += rotor2Rev + rotor2Rev;
                rotor3Rev = generateReverseSubst(rotors[settings.Model, settings.Rotor3]);
                rotor3Rev += rotor3Rev + rotor3Rev;
                if (settings.Rotor4 < 8)
                {
                    rotor4Rev = generateReverseSubst(rotors[settings.Model, settings.Rotor4]);
                    rotor4Rev += rotor4Rev + rotor4Rev;
                }
            }
            // configure rotor notches
            rotor1notches = notches[settings.Rotor1];
            rotor2notches = notches[settings.Rotor2];
            rotor3notches = notches[settings.Rotor3];
        }

        private char enigmacrypt(char keyPressed)
        {
            //1. Substitution with plugboard
            char entrySubst = this.settings.PlugBoard(keyPressed);

            //2. Spindle substitution
            char spindleSubst = enigmaSpindle(entrySubst);

            //3. Substitution with plugboard
            return this.settings.PlugBoard(spindleSubst);
        }

        private char enigmaSpindle(char entrySubst)
        {

            char ch = entrySubst;
            

            // get the position of each rotor
            char[] key = settings.Key.ToCharArray();
            int keyIndex = key.Length - 1;
            int rotor4Pos = 0;
            if (settings.Model == 4 && key.Length == 4)
            {
                // M4 "Shark" uses 4 rotors
                rotor4Pos = settings.Alphabet.IndexOf(key[0]);
            }
            int rotor1Pos = settings.Alphabet.IndexOf(key[keyIndex--]);
            int rotor2Pos = settings.Alphabet.IndexOf(key[keyIndex--]);
            int rotor3Pos = settings.Alphabet.IndexOf(key[keyIndex]);


            //check notches and update the rotor position
            keyIndex = key.Length - 1;
            foreach (char n in rotor1notches)
            {
                if (n == key[keyIndex--]) rotor2Pos = (rotor2Pos + 1) % aLength;    
            }

            foreach (char n in rotor2notches)
            {
                if (n == key[keyIndex--]) rotor3Pos = (rotor3Pos + 1) % aLength;
            }

            foreach (char n in rotor3notches)
            {
                if (n == key[keyIndex]) rotor4Pos = (rotor4Pos + 1) % aLength;
            }

            rotor1Pos = (rotor1Pos + 1) % aLength;
            keyIndex = key.Length - 1;
            
            key[keyIndex--] = A[rotor1Pos];
            key[keyIndex--] = A[rotor2Pos];
            key[keyIndex--] = A[rotor3Pos];
            if (settings.Model == 4 && key.Length == 4) key[keyIndex] = A[rotor4Pos];

            // write back the updated rotor settings
            settings.Key = new string(key);

            //add the ring-offset
            rotor1Pos = (aLength + rotor1Pos - settings.Ring1) % 26;
            rotor2Pos = (aLength + rotor2Pos - settings.Ring2) % 26;
            rotor3Pos = (aLength + rotor3Pos - settings.Ring3) % 26;

            // now do the substitution
            ch = A[aLength + A.IndexOf(rotor1For[aLength + A.IndexOf(ch) + rotor1Pos]) - rotor1Pos];
            ch = A[aLength + A.IndexOf(rotor2For[aLength + A.IndexOf(ch) + rotor2Pos]) - rotor2Pos];
            ch = A[aLength + A.IndexOf(rotor3For[aLength + A.IndexOf(ch) + rotor3Pos]) - rotor3Pos];
            ch = reflector[aLength + A.IndexOf(ch)];
            ch = A[aLength + A.IndexOf(rotor3Rev[aLength + A.IndexOf(ch) + rotor3Pos]) - rotor3Pos];
            ch = A[aLength + A.IndexOf(rotor2Rev[aLength + A.IndexOf(ch) + rotor2Pos]) - rotor2Pos];
            ch = A[aLength + A.IndexOf(rotor1Rev[aLength + A.IndexOf(ch) + rotor1Pos]) - rotor1Pos];

            return ch;
        }

        private string generateReverseSubst(string p)
        {
            char[] result = new char[aLength];

            for (int i = 0; i < aLength; i++)
            {
                result[i] = settings.Alphabet[p.IndexOf(settings.Alphabet[i])];
            }

            return new string(result);
        }

